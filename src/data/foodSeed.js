function createFood({
  id,
  name,
  localNames = {},
  brand = null,
  category = 'General',
  keywords = [],
  defaultServingLabel,
  defaultServingGrams = null,
  nutritionPerServing,
  nutritionPer100g = null,
  servingUnits = [],
  popularityScore = 0,
}) {
  return {
    id,
    name,
    localNames,
    normalizedName: name.toLowerCase().trim().replace(/[^a-z0-9가-힣]+/g, ' ').trim(),
    brand,
    category,
    keywords,
    defaultServingLabel,
    defaultServingGrams,
    nutritionPerServing,
    nutritionPer100g,
    servingUnits,
    popularityScore,
    verified: true,
    source: 'seed',
  }
}

