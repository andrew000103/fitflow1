export function createLegacySuggestion(food) {
  return {
    name: food.name,
    localNames: food.localNames,
    calories: food.nutritionPerServing.kcal,
    protein: food.nutritionPerServing.protein,
    carbs: food.nutritionPerServing.carbs,
    fat: food.nutritionPerServing.fat,
    favorite: food.popularityScore >= 90,
  }
}
